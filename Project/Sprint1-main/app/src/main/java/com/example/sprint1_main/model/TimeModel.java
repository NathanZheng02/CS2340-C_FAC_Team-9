package com.example.sprint1_main.model;

public class TimeModel {

    private int month;
    private int day;
    private int year;
    private int hour;
    private int minute;

    public TimeModel() {

    }

    public TimeModel(int month, int day, int year, int hour, int minute) {
        this.month = month;
        this.day = day;
        this.year = year;
        this.hour = hour;
        this.minute = minute;
    }
}
